    private void loadReceptionistId() {
       try{
           List<String> receptionistId=ReceptionistDao.getAllUserId();
           if(receptionistId.isEmpty()){
               JOptionPane.showMessageDialog(null, "Please Enter the ReceptionistId");
               return;
           }
           
           btnRemove.setEnabled(true);
           for(String userid : receptionistId){
               jcRecpId.addItem(userid);
           }
       }
       catch(SQLException e){
           JOptionPane.showMessageDialog(null, "DB Error", "Error",JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
       }
    }
