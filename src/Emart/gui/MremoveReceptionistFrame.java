    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
    
        String userId=jcRecpId.getSelectedItem().toString();
        try{
           boolean result=ReceptionistDao.deleteReceptionistId(userId);
           if(result){
               JOptionPane.showMessageDialog(null, "Receptionist deleted successfully!");
               jcRecpId.removeItemAt(jcRecpId.getSelectedIndex());
               if(jcRecpId.getItemCount()==0)
                   btnRemove.setEnabled(false);
               return;
           }
             JOptionPane.showMessageDialog(null, "Receptionist not deleted!");
           
       }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, "DB Error", "Error",JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

